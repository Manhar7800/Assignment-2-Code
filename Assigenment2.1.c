#include<stdio.h>
int main(){
	
	int age=25;// integer val
	char grade='A'; // character val
	float price=5.8;// float val
	const float pi=3.14;
	
	// display the val
	printf("\nage: %d",age);
	printf("\ngrade: %c",grade);
	printf("\nprice:%f",price);
	
	//display the constant value 
	
	printf("\nvalue of pi(constant):.%5\n",pi);
	return 0;
}
